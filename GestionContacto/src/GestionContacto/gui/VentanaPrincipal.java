    public void inicializarTabla() {
        dtm = new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"Nombre", "Apellido", "DNI", "Provicia"});
        jTable2.setModel(dtm);
        
        TableRowSorter<TableModel> ordena = new TableRowSorter<>(dtm);
        RowFilter<Object, Object> expresionRegular = RowFilter.regexFilter("1", 2);
        ordena.setRowFilter(expresionRegular);
        jTable2.setRowSorter(ordena);
    }

