    private void miBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miBajaActionPerformed
        this.tfError.setText("");
        this.dtm = (DefaultTableModel) this.jTable2.getModel();
        int[] fila = null;
        for (int i : fila) {
            int aux = 0;
            aux = fila[i];
            aux = this.jTable2.getSelectedRow();
            if (aux != -1) {
                dtm.removeRow(i);
            } else {
                this.tfError.setText("Selecciona una fila.");
            }
        }
    }//GEN-LAST:event_miBajaActionPerformed

